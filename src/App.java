import java.util.Scanner;
public class App {
    public static void main(String[] args) throws Exception {
        Scanner sc = new Scanner(System.in); // This is where the Scanner is defined as sc
        int num1 = 10; // Interger #1
        int num2 = 37; // Interger #2
        System.out.println("How old are you?"); // Asks User For Age
        String Age = sc.nextLine(); // Age Variable to store users age
        System.out.println("You are " + (Age) + " years old."); // Prints user age as a message
        System.out.println("The Sum Of The Two Given Numbers is: " + (num1 + num2)); // Combines the two intergers with a message


    }
}
